/* simple echo app */

public class Echo {
    public static void main(String[] args) {
        String rlt = null;

        for (int i = 1; i < args.length; i++) {
            rlt += args[i] + " ";
        }

        System.out.println(rlt);
    }
    public static doNothing() {
        // bug is fixed
    }


}
